package condicao;

public class exe_else {

	public static void main(String[] args) {
		
		int num1 = 7;
		int num2 = 9;
		
		if(num1 > num2) {
			//simple condiçao se num1 e maior que num2 então faça isso...
			System.out.println(num1 + " é maior que " + num2);
		}else {
			//se não faça isso...
			System.out.println(num1 + " Não maior que " + num2);
		}
		
	}
}
