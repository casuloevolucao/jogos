package array;

public class exemplo {
	public static void main(String[] args) {
		int[] idade = new int [12]; 
		
		//12 representao tamando do array lembrando que array começa pelo 0
		
		idade[0] = 20;
		idade[1] = 21;
		idade[2] = 22;
		idade[3] = 23;
		idade[4] = 24;
		
		System.out.println("posição 0: " + idade[0]);
		System.out.println("posição 1: " + idade[1]);
		System.out.println("posição 2: " + idade[2]);
		System.out.println("posição 3: " + idade[3]);
		System.out.println("posição 4: " + idade[4]);
	}
}
